def find_pairs(arr1, arr2, target):
    set1 = set(arr1)
    pairs = []
    for num in arr2:
        complement = target - num
        if complement in set1:
            pairs.append((complement, num))
    return pairs




arr1 = [1, 2]
arr2 = [3]
target = 7

pairs = find_pairs(arr1, arr2, target)
print (pairs)



