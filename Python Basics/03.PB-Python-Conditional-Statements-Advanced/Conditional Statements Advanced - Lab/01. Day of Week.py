wday = int(input())

if wday == 1 :
    print ("Monday")
elif wday == 2 :
    print ("Tuesday")
elif wday == 3 :
    print ("Wednesday")
elif wday == 4 :
    print ("Thursday")
elif wday == 5 :
    print ("Friday")
elif wday == 6 :
    print ("Saturday")
elif wday == 7 :
    print ("Sunday")
else :
    print ("Error")